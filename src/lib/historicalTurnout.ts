/** Get total historical turnout across all BMs for a given hour */
export function getHistoricalHourTotal(hour: string, year: "y2022" | "y2023"): number {
  let total = 0;
  const seen13 = new Set<number>();
  for (const stationId in HISTORICAL_TURNOUT) {
    const id = Number(stationId);
    // BM 13 and 14 share same old data - only count once
    if (id === 14) continue;
    const entry = HISTORICAL_TURNOUT[id]?.[hour];
    if (entry) total += entry[year];
  }
  return total;
}
